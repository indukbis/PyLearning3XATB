#Ternary Operator

pramod_marks=10
amit_marks=20

#pramod_marks>amit_marks do something -> print("pramod")
#amit_marks>pramod_marks do something else -> print("amit")

print("pramod" if pramod_marks > amit_marks else "Amit" )

if pramod_marks > amit_marks:
    print("pramod is the winner")
else:
    print("Amit is the winner")

    